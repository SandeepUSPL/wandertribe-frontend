                              The assessment gives a way to Destination Development Plan – where we enable governments, local authorities and communities create an execution plan. We help them define concrete milestones, plan for funds and select right vendors and stakeholders. We put in place the project governance. Destination development can be both a one time activity ranging from 2-12 months and an ongoing governance activity.

