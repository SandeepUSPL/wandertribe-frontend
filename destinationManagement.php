                          <p>Addressing the nation from the ramparts of the Red Fort on the 73rd Independence Day, Prime Minister Narendra Modi urged people to visit at least 15 tourist destinations within India by 2022, saying there is a "huge" scope to improve India's tourism sector.</p>

                          <h5 style="text-decoration: underline;">6 Reasons: Why “Strategic Planning and Destination Management Planning” is an Essential Tourism Activity </h5>
                          <h6>Tourism is disrupted.  Rural, small, quaint towns are now emerging as new destinations and competing with traditional tourist destinations.</h6>
                          <p>
                              Planning has become paramount to avoid disasterous situations caused by haphazard developments not just for new emerging destinations but also for existing destinations. Ecosensitive destinations in particluar need a strong planning to avoid natural disaster as well as things like like traffic congestion, problems with solid waste disposal, a shortage of water, electricity generation and utilisation. It is required to ensure that the created assets are sustainable and don’t disturb the natural assests of the place. Globally, the misuse and abuse of a public and natural resource being commonplace, this planning is quite important.
                              Thus strategic planning is an important comprehensive process for determining what a destination should become and the steps needed to make tourists visit the place again and to keep tourists happy.  Destination management is the coordinated management of all elements that make up a destination, including the infrastructure, stay, local transport, attractions, amenities, access, marketing and pricing.
                          </p>

                          <ol>
                              <li>
                                  Every destination is unique. Its stories are uniques and things you can do are unique too. A destination known for heritage and history will be different from a destination for water sports or religious tourism. A careful planning is needed to create its appeal and to attract right kind of tourists. A scenario based response plan must be put in place to manage low/high-volume as per seasonality
                              </li>
                              <li>
                                  Public sector (government) and private sectors have important roles that are not always clearly demarcated. A planning is needed to demarcate these roles and set clear expectations as well as
                              </li>
                              <li>
                                  Most places were never planned to handle the kind of tourism rush that they are witnessing now this is putting stress on local resources.  Tourism means town infrastructure like Tourist Information Centers/ Kiosks, beautification of local roads and spots, parking facilities, safe boarding and lodging facilities compliant to fire and earthquake safety, sanitation, sewage and waste disposal upgradation, water availability, fire station, hospital, new power grids to meet higher demand for electricity as well as local adminstration strengthening.
                              </li>
                              <li>
                                  A plan is needed to ensure compliance to “enviornment guidelines and sustainability”
                              </li>
                              <li>
                                  Sometimes, tourism development requires intesive intervational collaboration as it involves heavy outlays and investments in core infrastructure (e.g. all weather Char Dham Route). This leaves government with little resources for several other competing development activities , therefore, particularly need to undertake tourism planning to ensure availability of funds and expertise.  It is also needed in order to ensure the proper utilization of the finances so that maximum economic and other benefits may be derived.
                              </li>
                              <li>
                                  A number of tourist destinations have experienced a cycle of intense building activity and capital investment, witnessed a tourist boom, hit a hay day and then began to decline. To avoid eventual decline responsible planning and management are essential.
